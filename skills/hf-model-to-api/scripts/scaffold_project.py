def text_classification_files(args, id2label: dict | None) -> dict[str, str]:
    if id2label:
        # JSON object keys are always strings; normalize to int at load time so
        # lookups by predicted class index (an int) actually hit.
        override_literal = json.dumps(id2label, indent=8)
        override_comment = ""
    else:
        override_literal = "None"
