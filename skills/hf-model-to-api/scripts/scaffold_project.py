def image_generation_files(args, id2label: dict | None) -> dict[str, str]:
    model_py = '''\
import torch
from diffusers import DiffusionPipeline


class ModelBundle:
    def __init__(self, pipe, device: str):
        self.pipe = pipe
        self.device = device


def _resolve_device(device_setting: str) -> str:
    if device_setting != "auto":
        return device_setting
    if torch.cuda.is_available():
        return "cuda"
    if torch.backends.mps.is_available():
        return "mps"
    return "cpu"


def load_model(settings) -> ModelBundle:
    device = _resolve_device(settings.device)
    dtype = torch.float16 if device == "cuda" else torch.float32
    pipe = DiffusionPipeline.from_pretrained(settings.model_id, torch_dtype=dtype)
    pipe = pipe.to(device)
    return ModelBundle(pipe, device)


def warm_up(bundle: ModelBundle) -> None:
    # Tiny, cheap warm-up: 1 step at low resolution just to trigger CUDA kernel
    # compilation / graph warm-up, not to produce a usable image.
    generate(bundle, "warm-up", num_inference_steps=1, width=64, height=64)


def generate(bundle: ModelBundle, prompt: str, num_inference_steps: int, width: int, height: int):
    result = bundle.pipe(
        prompt,
        num_inference_steps=num_inference_steps,
        width=width,
        height=height,
    )
    return result.images[0]
'''

