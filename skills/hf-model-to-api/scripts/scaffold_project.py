def build_extra_settings(task: str, args) -> str:
    if task in ("text-classification", "token-classification"):
        return f"    max_input_length: int = {args.max_input_length}\n"
    return ""


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument("--model-id", required=True)
    parser.add_argument("--task", required=True, choices=TASK_CHOICES)
    parser.add_argument("--output-dir", required=True)
    parser.add_argument("--id2label-json", default=None, help="JSON string or path to a JSON file mapping label id -> label name (classification/NER only)")
    parser.add_argument("--max-batch-size", type=int, default=8)
    parser.add_argument("--max-input-length", type=int, default=512, help="Max token length for text tasks")
    parser.add_argument("--max-chars-per-text", type=int, default=10000, help="Max characters per input string (text tasks); over this returns 422")
    parser.add_argument("--max-request-mb", type=float, default=10.0, help="Max request body size in MB; over this returns 413")
    parser.add_argument("--port", type=int, default=8000)
    parser.add_argument("--title", default=None)
    args = parser.parse_args()

    max_request_bytes = int(args.max_request_mb * 1024 * 1024)

    out_dir = Path(args.output_dir)
    if out_dir.exists() and any(out_dir.iterdir()):
        print(f"Refusing to scaffold into non-empty directory: {out_dir}", file=sys.stderr)
        return 1

    title = args.title or f"{args.model_id} API"

    id2label = None
    if args.id2label_json:
        raw = args.id2label_json
        path = Path(raw)
        text = path.read_text() if path.is_file() else raw
        try:
            id2label = json.loads(text)
        except json.JSONDecodeError:
            print("Could not parse --id2label-json as JSON or a path to a JSON file.", file=sys.stderr)
            return 1

    builder = TASK_BUILDERS[args.task]
    task_files = builder(args, id2label)
    endpoint_doc = task_files.pop("__endpoint_doc__")

    files: dict[str, str] = {
        "app/__init__.py": INIT_PY,
        "app/core/__init__.py": INIT_PY,
        "app/api/__init__.py": INIT_PY,
        # title/model_id are embedded into generated Python source, so they must be
        # inserted as escaped literals (json.dumps) — never raw inside quotes, or a value
        # containing a quote/newline could break out and inject code into the module.
        "app/main.py": MAIN_PY.substitute(title=json.dumps(title)),
        "app/core/config.py": CONFIG_PY.substitute(
            model_id=json.dumps(args.model_id),
            max_batch_size=args.max_batch_size,
            max_request_bytes=max_request_bytes,
            extra_settings=build_extra_settings(args.task, args),
            port=args.port,
        ),
        "tests/test_health.py": TEST_HEALTH_PY,
        ".env.example": ENV_EXAMPLE.substitute(
            model_id=args.model_id,
            max_batch_size=args.max_batch_size,
            max_request_bytes=max_request_bytes,
            port=args.port,
        ),
        ".gitignore": GITIGNORE,
        "README.md": README.substitute(
            title=title,
            model_id=args.model_id,
            task=args.task,
            port=args.port,
            endpoint_doc=endpoint_doc,
        ),
        **task_files,
    }

    for rel_path, content in files.items():
        full_path = out_dir / rel_path
        full_path.parent.mkdir(parents=True, exist_ok=True)
        full_path.write_text(content, encoding="utf-8")

    print(f"Scaffolded {len(files)} files into {out_dir}")
    for rel_path in sorted(files):
        print(f"  {rel_path}")

    todos = [f for f, content in files.items() if "TODO" in content]
    if todos:
        print("\nFiles with TODO markers to review before running:")
        for t in todos:
            print(f"  {t}")

    return 0


if __name__ == "__main__":
    sys.exit(main())
